-- ① レコード件数
SELECT 'MV_EDINET_COMPANIES' AS mv_name, COUNT(*) AS row_count FROM RAW_DB.COMPANY_MATCHING.MV_EDINET_COMPANIES
UNION ALL
SELECT 'MV_JPX_COMPANIES',               COUNT(*)               FROM RAW_DB.COMPANY_MATCHING.MV_JPX_COMPANIES
UNION ALL
SELECT 'MV_NTA_COMPANIES',               COUNT(*)               FROM RAW_DB.COMPANY_MATCHING.MV_NTA_COMPANIES;

-- ② EDINET: サンプル5件 + NULL集計
SELECT * FROM RAW_DB.COMPANY_MATCHING.MV_EDINET_COMPANIES LIMIT 5;

SELECT
  COUNT(*) AS total,
  SUM(CASE WHEN EDINET_CODE      IS NULL THEN 1 ELSE 0 END) AS null_edinet_code,
  SUM(CASE WHEN COMPANY_NAME_JA  IS NULL OR COMPANY_NAME_JA  = '' THEN 1 ELSE 0 END) AS null_or_empty_name_ja,
  SUM(CASE WHEN COMPANY_NAME_EN  IS NULL OR COMPANY_NAME_EN  = '' THEN 1 ELSE 0 END) AS null_or_empty_name_en,
  SUM(CASE WHEN COMPANY_NAME_KANA IS NULL OR COMPANY_NAME_KANA = '' THEN 1 ELSE 0 END) AS null_or_empty_kana,
  SUM(CASE WHEN SECURITIES_CODE  IS NULL OR SECURITIES_CODE  = '' THEN 1 ELSE 0 END) AS null_or_empty_securities_code,
  SUM(CASE WHEN CORPORATE_NUMBER IS NULL OR CORPORATE_NUMBER = '' THEN 1 ELSE 0 END) AS null_or_empty_corporate_number
FROM RAW_DB.COMPANY_MATCHING.MV_EDINET_COMPANIES;

-- ③ JPX: サンプル5件 + NULL集計
SELECT * FROM RAW_DB.COMPANY_MATCHING.MV_JPX_COMPANIES LIMIT 5;

SELECT
  COUNT(*) AS total,
  SUM(CASE WHEN SECURITIES_CODE IS NULL OR SECURITIES_CODE = '' THEN 1 ELSE 0 END) AS null_or_empty_securities_code,
  SUM(CASE WHEN COMPANY_NAME    IS NULL OR COMPANY_NAME    = '' THEN 1 ELSE 0 END) AS null_or_empty_name,
  SUM(CASE WHEN MARKET          IS NULL OR MARKET          = '' THEN 1 ELSE 0 END) AS null_or_empty_market,
  SUM(CASE WHEN LISTED_DATE     IS NULL THEN 1 ELSE 0 END) AS null_listed_date
FROM RAW_DB.COMPANY_MATCHING.MV_JPX_COMPANIES;

-- ④ NTA: サンプル5件 + NULL集計
SELECT * FROM RAW_DB.COMPANY_MATCHING.MV_NTA_COMPANIES LIMIT 5;

SELECT
  COUNT(*) AS total,
  SUM(CASE WHEN CORPORATE_NUMBER IS NULL OR CORPORATE_NUMBER = '' THEN 1 ELSE 0 END) AS null_or_empty_corp_num,
  SUM(CASE WHEN COMPANY_NAME     IS NULL OR COMPANY_NAME     = '' THEN 1 ELSE 0 END) AS null_or_empty_name,
  SUM(CASE WHEN COMPANY_NAME_KANA IS NULL OR COMPANY_NAME_KANA = '' THEN 1 ELSE 0 END) AS null_or_empty_kana,
  SUM(CASE WHEN COMPANY_NAME_EN  IS NULL OR COMPANY_NAME_EN  = '' THEN 1 ELSE 0 END) AS null_or_empty_name_en,
  SUM(CASE WHEN COMPANY_TYPE     IS NULL OR COMPANY_TYPE     = '' THEN 1 ELSE 0 END) AS null_or_empty_company_type,
  SUM(CASE WHEN CLOSED_DATE      IS NOT NULL THEN 1 ELSE 0 END) AS closed_companies
FROM RAW_DB.COMPANY_MATCHING.MV_NTA_COMPANIES;

-- ⑤ NTA: PROCESS / CORRECT の値分布（変更種別の把握）
SELECT PROCESS, CORRECT, COUNT(*) AS cnt
FROM RAW_DB.COMPANY_MATCHING.MV_NTA_COMPANIES
GROUP BY PROCESS, CORRECT
ORDER BY cnt DESC
LIMIT 20;

-- ⑥ EDINET: SUBMITTER_TYPE / LISTING_STATUS の分布
SELECT SUBMITTER_TYPE, LISTING_STATUS, COUNT(*) AS cnt
FROM RAW_DB.COMPANY_MATCHING.MV_EDINET_COMPANIES
GROUP BY SUBMITTER_TYPE, LISTING_STATUS
ORDER BY cnt DESC;

-- ⑦ EDINET × JPX: SECURITIES_CODEで突合可能か確認
SELECT
  COUNT(DISTINCT e.SECURITIES_CODE) AS edinet_with_securities_code,
  COUNT(DISTINCT j.SECURITIES_CODE) AS jpx_codes,
  COUNT(DISTINCT e.SECURITIES_CODE) - COUNT(DISTINCT CASE WHEN j.SECURITIES_CODE IS NOT NULL THEN e.SECURITIES_CODE END) AS unmatched_in_jpx
FROM RAW_DB.COMPANY_MATCHING.MV_EDINET_COMPANIES e
LEFT JOIN RAW_DB.COMPANY_MATCHING.MV_JPX_COMPANIES j
  ON e.SECURITIES_CODE = j.SECURITIES_CODE
WHERE e.SECURITIES_CODE IS NOT NULL AND e.SECURITIES_CODE != '';

-- ⑧ EDINET × NTA: CORPORATE_NUMBERで突合可能か確認
SELECT
  COUNT(DISTINCT e.CORPORATE_NUMBER) AS edinet_with_corp_num,
  COUNT(DISTINCT n.CORPORATE_NUMBER) AS nta_corp_num,
  COUNT(DISTINCT CASE WHEN n.CORPORATE_NUMBER IS NOT NULL THEN e.CORPORATE_NUMBER END) AS matched_count
FROM RAW_DB.COMPANY_MATCHING.MV_EDINET_COMPANIES e
LEFT JOIN RAW_DB.COMPANY_MATCHING.MV_NTA_COMPANIES n
  ON e.CORPORATE_NUMBER = n.CORPORATE_NUMBER
WHERE e.CORPORATE_NUMBER IS NOT NULL AND e.CORPORATE_NUMBER != '';
